    private void readTXT()
    {
        try
        {
 
            File file = new File(filename);
            
            FileReader fr = new FileReader(file);
            
            BufferedReader br = new BufferedReader(fr);
         
            String line = "";
            String[] tempArr;
            while ( (line = br.readLine()) != null)
            {
                tempArr = line.split(delimiter);

                
                    Employee empl=new Employee();
                    empl.setName(tempArr[1]);
                    empl.setAddress(tempArr[2]);
                    empl.setEthnicity(tempArr[3]);
                    empl.setGender(tempArr[4]);
                    empl.setPhoneNumber(tempArr[5]);
                    empl.setHours(Double.parseDouble(tempArr[6]));
                    empl.setPayRate(Double.parseDouble(tempArr[7]));
                    empl.setInsurance(Boolean.parseBoolean(tempArr[8]));
                    empl.setEmployeeID(tempArr[9]);
                    empl.setUntaxedWage();
                    empl.setTaxedWage();
                    employees.add(empl);
                System.out.println();
            }
            br.close();
        }        
    
      

        catch(IOException e)
        {
            JOptionPane.showMessageDialog(this, "Error reading the file");
        }           
            
        
    }
