    public int searchEmpID(String ID, ArrayList<Employee> employees)					// searches ids and returns the index they are located
	{
		int index = -1;																		
		for (int i = 0; i< employees.size(); i++)
		{
			if (employees.get(i).getEmployeeID().equals(ID))													
			{
				index = i;
			}
		}
		
		return index;
	}
